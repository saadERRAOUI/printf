#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
		(*len) += write(1, "(null)", 6);
	else
	{
		while (s[i])
		{
			ft_putchar(s[i], len);
			i++;
		}
	}
}
